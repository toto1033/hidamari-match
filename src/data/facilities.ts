export async function getFacilityById(id: string): Promise<Facility | null> {
  return facilities.find((f) => f.id === id) ?? null;
}

export async function searchFacilities(params: SearchParams): Promise<Facility[]> {
  return facilities.filter((f) => {
    if (params.prefecture && f.prefecture !== params.prefecture) return false;
    if (params.disabilities?.length && !params.disabilities.some((d) => f.disabilities.includes(d as Disability))) return false;
    if (params.ageGroups?.length && !params.ageGroups.some((a) => f.ageGroups.includes(a as AgeGroup))) return false;
    if (params.supportTypes?.length && !params.supportTypes.some((s) => f.supportTypes.includes(s as SupportType))) return false;
    if (params.hasTransport && !f.hasTransport) return false;
    if (params.hasVacancy && f.vacancyCount === 0) return false;
    if (params.keyword) {
      const kw = params.keyword.toLowerCase();
      if (
        !f.name.toLowerCase().includes(kw) &&
        !f.city.toLowerCase().includes(kw) &&
        !f.description.toLowerCase().includes(kw) &&
        !f.catchcopy.toLowerCase().includes(kw)
      ) return false;
    }
    return true;
  });
}
